idade: int = int(input('Digite sua idade: '))
temp_servi = int = int(input('Digite por quanto tempo você trabalhou: '))
if idade >= 65:
    print(f'Você tem {idade} já pode se aposentar')
elif temp_servi >= 30:
    print(f'Você tem {temp_servi} anos de trabalho, já pode se aposentar')
elif idade == 60 and temp_servi >= 25:
    print(f'Você tem {idade} anos e trabalhou por {temp_servi} já pode se aposentar')
