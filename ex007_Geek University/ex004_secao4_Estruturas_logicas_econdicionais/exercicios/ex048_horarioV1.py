"""
Leia um valor inteiro em segundos, e imprima-o em horas, minutos e segundos.
"""
segundos = int(input('Digite os segundos: '))
print(f'{segundos} segundos em horas é: {segundos / 3600}')
print(f'{segundos} Segundos em minutos é: {segundos / 60}')
print(f'{segundos} segundos em segundos é: {segundos}')
